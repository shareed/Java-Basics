package com.revature;

public class ForLoop {
	public static void main(String[] args) {
		int total = 0;
		//For Loop
				for(int i = 0; i < 5; i++) {
					total = total + 37;
					System.out.println("total: " + total);
					System.out.println("i: " + i);
				}
				
				System.out.println("________________________________________________");
				System.out.println("                                                ");
		
	}

}

